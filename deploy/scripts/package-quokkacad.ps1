=============================

This folder is generated by:
  deploy/scripts/package-quokkacad.ps1

Resources included:
  - cad_bridge
  - npwd_vicroads
  - npwd_fines_victoria

Server.cfg example:
  ensure npwd
  ensure [quokkacad]

NPWD app config must include:
  "apps": [
    "npwd_vicroads",
    "npwd_fines_victoria"
  ]

Rebuild this pack after changing cad_bridge or either NPWD app.
"@
[System.IO.File]::WriteAllText((Join-Path $outputRoot 'README.txt'), $readme, [System.Text.Encoding]::UTF8)

Write-Host "`nPackaged resources into: $outputRoot" -ForegroundColor Green
Get-ChildItem -LiteralPath $outputRoot -Directory | Select-Object Name, FullName | Format-Table -AutoSize
